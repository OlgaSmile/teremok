function getNightPriceText(number) {
  if (!number) {
    return "за ніч";
  }
  let text = "за ";

  const lastDigit = number % 10;
  const lastTwoDigits = number % 100;

  if (lastTwoDigits >= 11 && lastTwoDigits <= 14) {
    text += number + " ночей";
  } else if (lastDigit === 1) {
    text += number + " ніч";
  } else if (lastDigit >= 2 && lastDigit <= 4) {
    text += number + " ночі";
  } else {
    text += number + " ночей";
  }

  return text;
}

